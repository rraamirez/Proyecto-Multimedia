    /**
     * Función cuadrática f(x) = 1/100(x-m)² siendo 0 <= m <= 255
     *
     * @param m
     * @return
     */
    private LookupTable cuadratica(double m) {
        double k = 100.0 * 255.0;
        byte[] data = new byte[256];
        for (int i = 0; i < 256; i++) {
            int v = (int) (((i - m) * (i - m) / k) * 255.0);
            if (v < 0) {
                v = 0;
            }
            if (v > 255) {
                v = 255;
            }
            data[i] = (byte) v;
        }
        return new ByteLookupTable(0, data);
    }


