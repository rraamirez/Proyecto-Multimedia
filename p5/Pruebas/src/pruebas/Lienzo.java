    public void paint(Graphics g) {
        super.paint(g);
        Graphics2D g2d=(Graphics2D)g;
        // LÃ­nea
        Point2D p1=new Point2D.Float(70,70);
        Point2D p2=new Point2D.Float(200,200);
        Line2D linea = new Line2D.Float(p1,p2);
        g2d.draw(linea);
        
        Arc2D arco = new Arc2D.Float(23,67,98,123,200,345,1);
        g2d.draw(arco);
        
        QuadCurve2D quad = new QuadCurve2D.Float(45,65,89, 65, 25, 45);
        g2d.draw(quad);
        
        Shape trazo = new GeneralPath(quad);
        
        // draw GeneralPath (polygon)
            int x1Points[] = {0, 1000, 0, 1000};
            int y1Points[] = {0, 500, 500, 0};
            GeneralPath polygon = 
                    new GeneralPath(GeneralPath.WIND_EVEN_ODD,
                                    x1Points.length);
            polygon.moveTo(x1Points[0], y1Points[0]);

            for (int index = 1; index < x1Points.length; index++) {
                    polygon.lineTo(x1Points[index], y1Points[index]);
            };

            polygon.closePath();
            g2d.draw(polygon);
        
    }

    
    
    
    
    
    
    
    
